####Ademas de los operadores matematicos estan los logicos que nos dan verdad o falsedad
####de uno o mas elementos
##Son el a<b, a>b, a<=b, a>=b
#ejemplo
print(3<4)
print(1>2)
print(4<=5)
print(7>=7)

##Estan las operaciones de comparacion como el "==" y el "!=" y para unir mas
##comparaciones estan el or y and. Ejemplo
print(3==4)
print(2!=3)
print(2==2 and 3==3 and 4==4)
#El problema del and es que con una sola comparacion que sea falsa toda la cadena sera falsa
#y para eso esta el or que con solo una comparacion sea verdad la respuesta global sera true
#ejemplo
print(2==2 and 3==2 and 4==4)
print(2==2 or 3==3 or 4==2)





